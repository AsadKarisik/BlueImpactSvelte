<div class="one">
  <h1>Urban Logistics is contributing to the global warming</h1>
  <p>
    Urban freight is responsible for 25% of urban transport related CO2
    emissions and 30 to 50% of other transport related pollutants-Particulate
    matter and Nitrogen Oxide. <a
      href="https://www.etp-logistics.eu/wp-content/uploads/2022/08/Urban-Freight-Roadmap.pdf."
      target="_blank"
      rel="noreferrer">(Source: ALICE)</a
    >
  </p>

  <h2>
    Urban Logistics is contributing to the global warming with its
    inefficiencies
  </h2>
  <p>
    The logistics inefficiencies occupy productive time of vehicles engaged. The
    more time is lost for logistics inefficiencies, the more additional
    productive time of vehicles is necessary. That means, more working hours for
    vehicles engaged or more new vehicles and consequently more costs and more
    pollution.
  </p>
</div>

